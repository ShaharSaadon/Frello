<template>
  <div class="task-head-tags">
    <div v-if="members?.length">
      <h3 class="title">Members</h3>
      <div class="head-members">
        <img @click="$emit('openModal', 'MemberPicker')" v-for="member in members" :src="member.imgUrl" class="member-profile" />
      </div>
    </div>
    <div v-if="labels?.length">
      <h3 class="title">Labels</h3>
      <div class="head-labels">
        <div @click="$emit('openModal', 'LabelPicker')" v-for="label in labels" class="label-tag" :class="label.color">{{ label.title }}</div>
      </div>
    </div>
    <div>
      <h3 class="title">Notifications</h3>
      <button @click="$emit('toggleWatch')" class="notifications">
        <span></span> {{ task.isWatch ? 'Watching' : 'Watch' }}
      </button>
    </div>
    <div v-if="task?.dueDate">
      <h3 class="title">Due date</h3>
      <button class="notifications">{{ getDate }}</button>
    </div>
  </div>
</template>

<script>
export default {
  name: 'TaskHeadTags',
  props: {
    task: {
      type: Object,
      required: true,
    },
  },
  data() {
    return {}
  },
  methods: {},
  computed: {
    labels() {
      return this.$store.getters.labels.filter((label) => {
        if (this.task.labels.includes(label.id)) return label
      })
    },
    getDate() {
      return new Date(this.task.dueDate).toLocaleString('en-GB', { timeZone: 'UTC' })
    },
    members() {
      return this.$store.getters.watchedBoard.members.filter((m) => {
        if (this.task.members?.includes(m._id)) return m
      })
    },
  },
  created() {},
  components: {},
}
</script>
